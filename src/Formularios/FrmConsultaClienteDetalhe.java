    private void lucroVendasPeriodo(int idCliente){
        
        DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        
       try{ 
        String sql = "SELECT SUM(VLR_FINAL) LUCRO_GERADO_PERIODO FROM FLUXOCAIXA WHERE PAGO = 'S' AND OPERACAO = 'V' AND " +
                     "DATA_PGTO >= ? AND DATA_PGTO <= ? and id_cliente = ?";
        
            PreparedStatement ps = this.conexao.prepareStatement(sql);
            ps.setString(1, format.format(dtaInicial.getDate()));
            ps.setString(2, format.format(dtaFinal.getDate()));
            ps.setInt(3, idCliente);
            ResultSet rsts = ps.executeQuery();
            
            rsts.last();
            
            txtLucroGeradoPeriodo.setText(df.format(rsts.getDouble("LUCRO_GERADO_PERIODO")));
            
            String sql2 = "select count(id) vendas from venda_cab where data >= ? and data <= ? and id_cliente = ?"; 
            PreparedStatement ps2 = this.conexao.prepareStatement(sql2);
            ps2.setString(1, format.format(dtaInicial.getDate()));
            ps2.setString(2, format.format(dtaFinal.getDate()));
            ps2.setInt(3, idCliente);
            ResultSet rsts2 = ps2.executeQuery();

            rsts2.next();

            txtVendasRealizadasPeriodo.setText(rsts2.getString("vendas"));
     
            
        } catch (SQLException ex) {
            Logger.getLogger(FrmConsultaClienteDetalhe.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    private void geraRelConsultaClientes(){
        
        try {
            
            
            
            
            this.conexao = Conexao.getConexao();
            
            String sql = "Select cl.id, cl.nome, cl.data, cl.uf, cl.fone1, cl.fone2, CONCAT_WS(', ', cl.endereco, cl.numero) endereco, ci.nome cidade, cl.datanasc,"
                    + " CASE status WHEN 'R' THEN 'Regular'"
                    + " ELSE 'Irregular' END status from clientes cl inner join"
                    + " cidades ci on cl.id_cidade = ci.id where cl.nome = ?";
            PreparedStatement ps = this.conexao.prepareStatement(sql);
            ps.setString(1, txtNomeCliente.getText());
            ResultSet rsts = ps.executeQuery();
            
            JRResultSetDataSource tst= new JRResultSetDataSource(rsts);
            JasperReport jr = (JasperReport)JRLoader.loadObject(getClass().getResource("/Relatorios/relConsultaClientes.jasper"));
            Map map = new HashMap();
            
            map.put("vendasRealizadas", Integer.parseInt(txtVendasRealizadas.getText()));
            map.put("parcelasPendentes", Integer.parseInt(txtParcelasPendentes.getText()));
            map.put("parcelasVencidas", Integer.parseInt(txtVencidas.getText()));
            map.put("dataUltimaVenda", txtDataUltimaVenda.getText());
            map.put("lucroGerado", txtLucroGerado.getText());
            map.put("dividaAtual", txtDivida.getText());
            
            JasperPrint jp = JasperFillManager.fillReport(jr,map,tst);
            new GeraRelatorio().verRelat("Relatório de Consulta de Clientes", jp);
            
        } catch (JRException ex) {
            Logger.getLogger(FrmConsultaClienteDetalhe.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrmConsultaClienteDetalhe.class.getName()).log(Level.SEVERE, null, ex);
        }

        
        
        
    }
    
   
    
    
