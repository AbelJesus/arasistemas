    public void retornaDados(String nome){
        
        try {
            this.conexao = Conexao.getConexao();
            
            String sql = "Select cl.id, cl.nome, cl.data, cl.status, cl.fone1, cl.fone2, cl.endereco, ci.nome cidade, cl.datanasc from clientes cl inner join"
                    + " cidades ci on cl.id_cidade = ci.id where cl.nome = ?";
            PreparedStatement ps = this.conexao.prepareStatement(sql);
            ps.setString(1, nome);
            ResultSet rsts = ps.executeQuery();
            resultadoCliente = rsts;
            rsts.next();
            
               
            idCliente = (rsts.getInt("cl.id"));
            
            txtCidade.setText(rsts.getString("cidade"));
            txtTel1.setText(rsts.getString("cl.fone1"));
            txtTel2.setText(rsts.getString("cl.fone2"));
            txtNasc.setText(dataFor.format(rsts.getDate("cl.datanasc")));
            txtEndereco.setText(rsts.getString("cl.endereco"));
            txtNomeCliente.setText(rsts.getString("cl.nome"));
   
            txtDataCadastro.setText(dataFor.format(rsts.getDate("cl.data")));
            //txtParcelasPendentes.setText(rsts.getString("parcelas"));
            //txtDivida.setText(df.format(rsts.getDouble("divida")));
            
            if("R".equals(rsts.getString("cl.status"))){
                        txtStatus.setForeground(Color.green);
                        txtStatus.setText("Regular");
                    }else{
                        txtStatus.setForeground(Color.red);
                        txtStatus.setText("Irregular");
                    }
            
            this.setVisible(true);
            
            
        } catch (SQLException ex) {
         
            JOptionPane.showMessageDialog(null, "Nenhum dado resgistrado !", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }
    
    private void parcVencidas(int idcliente){
        
        try {
            this.conexao = Conexao.getConexao();
            
            String sql = "Select  COUNT(nparc) vencidas from fluxocaixa where data_vencto < date(now()) and pago = 'N' and id_cliente = ?";
            PreparedStatement ps = this.conexao.prepareStatement(sql);
            ps.setInt(1, idcliente);
            ResultSet rsts = ps.executeQuery();
            
            if(rsts.next()){
                txtVencidas.setText(rsts.getString("Vencidas"));
            }else{
                txtVencidas.setText("0");
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(FrmConsultaClienteDetalhe.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }
    
